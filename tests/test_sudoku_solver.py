import pytest
from sudolu_solver.sudoku_solver import init_board

@pytest.mark.parametrize('input', 
                         ['board = ' +
                          '[["5","3",".",".","7",".",".",".","."],' +
                          '["6",".",".","1","9","5",".",".","."],' +
                          '[".","9","8",".",".",".",".","6","."],' +
                          '["8",".",".",".","6",".",".",".","3"],' +
                          '["4",".",".","8",".","3",".",".","1"],' +
                          '["7",".",".",".","2",".",".",".","6"],' +
                          '[".","6",".",".",".",".","2","8","."],' +
                          '[".",".",".","4","1","9",".",".","5"],' +
                          '[".",".",".",".","8",".",".","7","9"]]'])
def test_init_board(input):
  assert init_board(input) == [["5","3",".",".","7",".",".",".","."],
                               ["6",".",".","1","9","5",".",".","."],
                               [".","9","8",".",".",".",".","6","."],
                               ["8",".",".",".","6",".",".",".","3"],
                               ["4",".",".","8",".","3",".",".","1"],
                               ["7",".",".",".","2",".",".",".","6"],
                               [".","6",".",".",".",".","2","8","."],
                               [".",".",".","4","1","9",".",".","5"],
                               [".",".",".",".","8",".",".","7","9"]]

